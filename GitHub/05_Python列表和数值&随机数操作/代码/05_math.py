
# math: 数学
import math
# 数学操作

#    sum(): 求和
print(sum([1, 2]))
print(sum(range(1, 101)))
#    max()：最大值
print(max(1, -2))
print(max([1, -2]))
#    min(): 最小值
print(max([1, -2]))
#    round(): 四舍五入
print(round(1.513123, 2))#保留两位小数
print(math.ceil(10.2))#向上取整
print(math.floor(10.2))#向下取整
#    abs(): 绝对值
print(abs(-5))
print(math.fabs(-5))
